    private void clock(){ //Hàm hiển thị giờ ra màn hình
        new Thread(){
            public void run(){
                while (true){
                Calendar ca = new GregorianCalendar();
                int AM_PM = ca.get(Calendar.AM_PM);
                String day_night;
                if(AM_PM ==1){
                    day_night = "PM";
                }else{
                    day_night = "AM";
                }
                SimpleDateFormat sp = new SimpleDateFormat("HH:mm:ss");
                Date dat = ca.getTime();
                String time = sp.format(dat);
                lbrClock.setText(time + " " + day_night); //Đặt nội dung hiển thị cho dòng "Giờ"
            }    
            }      
        }.start();
    }

