    private void btnDangnhapDNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDangnhapDNActionPerformed
        // TODO add your handling code here:
        String username = txtUsernameDN.getText();
        String password = txtPasswordDN.getText();
        String type = cbType.getSelectedItem().toString();
        String sqlquery = "SELECT * FROM Taikhoan WHERE UserName=? AND uPassWord=? AND uType=?";
        try { //3 dòng đầu là câu lệnh để kết nối với SQL
        Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
        String url = "jdbc:sqlserver://localhost\\HP:1433;databaseName=qlytvien;encrypt=true;trustServerCertificate=true;user=sa;password=572311";
        Connection con = DriverManager.getConnection(url);
        PreparedStatement ps = con.prepareCall(sqlquery); //Thực thi câu truy vấn trong SQL
        ps.setString(1, username);
        ps.setString(2, password);
        ps.setString(3,type);
        ResultSet rs = ps.executeQuery(); //Lấy dữ liệu từ SQL bằng con trỏ
        if(txtUsernameDN.getText().equals("")){ //So sánh dòng Username với giá trị "rỗng"
            JOptionPane.showMessageDialog(this, "Bạn chưa điền Username");
            return;
        }else{
            if(txtPasswordDN.getText().equals("")){
                JOptionPane.showMessageDialog(this, "Bạn chưa điền Password");
                return;
        }else{
        if(rs.next()){ //So sánh dữ liệu lấy ra từ SQL với dữ liệu người dùng nhập vào
            JOptionPane.showMessageDialog(this, "Đăng nhập thành công");
            if(cbType.getSelectedIndex()==0){ //Nếu tư cách người dùng trùng với dòng "uType" trong bảng thì chuyển qua giao diện tương ứng
                frmGiaodienAdmin fr = new frmGiaodienAdmin();
                fr.setVisible(true);
                this.setVisible(false);
            }else{
                frmGiaodienUser fr = new frmGiaodienUser();
                fr.setVisible(true);
                this.setVisible(false);
            }
        }else{
            JOptionPane.showMessageDialog(this, "Sai Username hoặc Password");
            return;
        }     
        }}}catch (Exception e) {
            
        }
        txtUsernameDN.setText("");
        txtPasswordDN.setText("");
    }//GEN-LAST:event_btnDangnhapDNActionPerformed

